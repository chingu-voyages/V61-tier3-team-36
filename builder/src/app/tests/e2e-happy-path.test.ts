// Test
// ---------------------------------------------------------------------

import { POST as createWorkspaceRoute } from "../api/workspaces/route";
import { POST as createProjectRoute } from "../api/projects/route";
import { POST as chatRoute } from "../api/chat/route";
import { GET as getSpecRoute, POST as generateSpecRoute } from "../api/spec/route";

function request(method: string, path: string, options: {
  token?: string;
  aiKey?: string;
  body?: unknown;
} = {}) {
  return new Request(`http://localhost${path}`, {
    method,
    headers: {
      ...(options.token ? { "X-Workspace-Token": options.token } : {}),
      ...(options.aiKey ? { "X-AI-Model-Key": options.aiKey } : {}),
      ...(options.body ? { "Content-Type": "application/json" } : {}),
    },
    body: options.body ? JSON.stringify(options.body) : undefined,
  });
}

describe("E7-S1: end-to-end happy path (scripted fake LLM client)", () => {
  beforeEach(() => {
    vi.resetAllMocks();
  });

  it("drives workspace -> project -> interview to convergence -> generate spec -> fetch latest", async () => {
    const workspace = {
      id: "workspace-e2e",
      magic_token: "e2e-token",
      created_at: new Date(),
    };
    const project = {
      id: "project-e2e",
      workspace_id: "workspace-e2e",
      name: "E2E Test Project",
      status: "active",
      created_at: new Date(),
    };

    // In-memory conversation state that our mocked repository reads/writes,
    // so each chat turn sees the result of the previous one.
    let conversation = {
      id: "conversation-e2e",
      project_id: project.id,
      messages: [] as Anthropic.MessageParam[],
      interview_state: { satisfiedSectionIds: [] as string[] },
    };

    repository.createWorkspace.mockResolvedValue(workspace);
    repository.getWorkspaceByMagicToken.mockResolvedValue(workspace);
    repository.createProject.mockResolvedValue(project);
    repository.getProjectInWorkspace.mockResolvedValue(project);
    repository.getOrCreateConversation.mockImplementation(async () => conversation);
    repository.saveConversationTurn.mockImplementation(
      async (_projectId: string, messages: Anthropic.MessageParam[], state: { satisfiedSectionIds: string[] }) => {
        conversation = { ...conversation, messages, interview_state: state };
      }
    );

    // 1. Create workspace
    const workspaceResponse = await createWorkspaceRoute();
    expect(workspaceResponse.status).toBe(201);

    // 2. Create project
    const projectResponse = await createProjectRoute(
      request("POST", "/api/projects", {
        token: "e2e-token",
        body: { name: "E2E Test Project" },
      })
    );
    expect(projectResponse.status).toBe(201);

    // 3. Run chat turns until convergence (one section satisfied per turn)
    let converged = false;
    let safetyCounter = 0;
    while (!converged && safetyCounter < SPEC_SECTIONS.length + 2) {
      const chatResponse = await chatRoute(
        request("POST", "/api/chat", {
          token: "e2e-token",
          aiKey: "fake-key",
          body: { projectId: project.id, message: "Some answer." },
        })
      );
      expect(chatResponse.status).toBe(200);
      const body = await chatResponse.json();
      converged = body.converged;
      safetyCounter += 1;
    }
    expect(converged).toBe(true);

    // 4. Generate spec
    repository.createSpecVersion.mockImplementation(
      async (projectId: string, markdown: string, sections: Record<string, string>) => ({
        id: "spec-e2e",
        project_id: projectId,
        markdown,
        schema_json: sections,
        version: 1,
        created_at: new Date(),
      })
    );

    const generateResponse = await generateSpecRoute(
      request("POST", "/api/spec", {
        token: "e2e-token",
        aiKey: "fake-key",
        body: { projectId: project.id },
      })
    );
    expect(generateResponse.status).toBe(201);
    const generated = await generateResponse.json();
    expect(generated.version).toBe(1);
    expect(generated.markdown).toContain("Fake E2E Spec");

    // 5. Fetch latest spec
    repository.getLatestSpec.mockResolvedValue({
      id: "spec-e2e",
      project_id: project.id,
      markdown: generated.markdown,
      schema_json: {},
      version: 1,
      created_at: new Date(),
    });

    const fetchResponse = await getSpecRoute(
      request("GET", `/api/spec?projectId=${project.id}`, { token: "e2e-token" })
    );
    expect(fetchResponse.status).toBe(200);
    const fetched = await fetchResponse.json();
    expect(fetched.version).toBe(1);
  });
});
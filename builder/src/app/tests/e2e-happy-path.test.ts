import Anthropic from "@anthropic-ai/sdk";
import { beforeEach, describe, expect, it, vi } from "vitest";
import { SPEC_SECTIONS } from "../../../lib/spec-sections";
import type { LLMClient } from "../../../lib/llm-client";

/**
 * E7-S1 — End-to-end happy-path verification
 *
 * Drives the full backend loop using a scripted fake LLMClient (per the
 * E2-S3 abstraction) instead of a real AI provider:
 *
 *   create workspace -> project -> interview to convergence
 *   -> generate spec -> fetch latest
 *
 * No real API calls are made. The fake client is injected by mocking
 * createLLMClient(), so InterviewEngine and generateSpec() run for real —
 * only the AI provider call itself is faked.
 */

// ---------------------------------------------------------------------
// Scripted fake LLM client
// ---------------------------------------------------------------------

/**
 * Returns a fresh fake LLMClient. Each call to createMessage():
 *  - If asked for the "update_interview" tool: satisfies exactly one more
 *    spec section per call (in SPEC_SECTIONS order), until all are done.
 *  - If asked for the "emit_spec" tool: returns a complete fake spec.
 *
 * This stays in sync automatically with however many sections
 * SPEC_SECTIONS actually has — nothing is hardcoded.
 */
function createScriptedFakeLLMClient(): LLMClient {
  return {
    async createMessage({ systemPrompt, toolChoice }) {
      const toolName =
        toolChoice && toolChoice.type === "tool" ? toolChoice.name : undefined;

      if (toolName === "emit_spec") {
        return {
          id: "fake_spec_msg",
          type: "message",
          role: "assistant",
          content: [
            {
              type: "tool_use",
              id: "fake_tool_emit_spec",
              name: "emit_spec",
              input: {
                markdown: "# Fake E2E Spec\n\nGenerated by the scripted fake client for the E7-S1 test.",
                sections: Object.fromEntries(
                  SPEC_SECTIONS.map((s) => [s.id, `Fake content for ${s.label}`])
                ),
              },
            },
          ],
          model: "fake-scripted-model",
          stop_reason: "tool_use",
          stop_sequence: null,
          usage: { input_tokens: 0, output_tokens: 0 },
        } as Anthropic.Message;
      }

      // Interview turn: the real InterviewEngine's system prompt lists the
      // still-unsatisfied sections as "(id: xxx)". Satisfy the first one
      // mentioned. This reads real state from the prompt instead of relying
      // on a counter, so it works correctly even though a brand-new fake
      // client instance is created on every chat turn.
      const match = systemPrompt.match(/\(id:\s*([a-zA-Z0-9_-]+)\)/);
      const nextSectionId = match ? match[1] : SPEC_SECTIONS[0].id;

      return {
        id: `fake_interview_msg_${nextSectionId}`,
        type: "message",
        role: "assistant",
        content: [
          {
            type: "tool_use",
            id: `fake_tool_update_interview_${nextSectionId}`,
            name: "update_interview",
            input: {
              satisfied_section_ids: [nextSectionId],
              next_question: `Fake question about ${nextSectionId}`,
            },
          },
        ],
        model: "fake-scripted-model",
        stop_reason: "tool_use",
        stop_sequence: null,
        usage: { input_tokens: 0, output_tokens: 0 },
      } as Anthropic.Message;
    },
  };
}

// ---------------------------------------------------------------------
// Mocks: DB-layer repository functions stay mocked (same pattern as the
// existing routes.test.ts), but the LLM client is the scripted fake —
// InterviewEngine and generateSpec run for real against it.
// ---------------------------------------------------------------------

